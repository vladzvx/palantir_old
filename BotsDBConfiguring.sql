create table messages (
    message_db_id bigserial,
    time timestamp default CURRENT_TIMESTAMP not null ,
    user_id bigint,
    text text,
    state int,
    primary key (message_db_id,time),
    foreign key (user_id) references users (id)
) partition by RANGE (time);

create table search_results(
    id bigserial,
    request_id bigint,
    request_time timestamp ,
    time timestamp default CURRENT_TIMESTAMP not null ,
    link text,
    text text,
    page int,
    channel_id bigint,
    primary key (id,request_time),
    foreign key (request_id,request_time) references messages (message_db_id,time)
) partition by RANGE (request_time);

create table buttons(
    time timestamp default CURRENT_TIMESTAMP not null,
    button_id1 bigint,
    button_id2 bigint,
    request_id bigint,
    action_id int references actions (id),
    primary key (button_id1,button_id2,time),
    foreign key (request_id,time) references messages (message_db_id,time)
) partition by RANGE (time);

create table buttons_clicks(
    id bigserial,
    time timestamp default CURRENT_TIMESTAMP not null,
    click_time timestamp default CURRENT_TIMESTAMP not null,
    button_id1 bigint,
    button_id2 bigint,
    primary key (id,click_time),
    foreign key (button_id1,button_id2,time) references buttons (button_id1,button_id2,time)
) partition by RANGE (click_time);


create index users_ids_in_mess on messages using hash(user_id);
create index users_ids_in_results on search_results using hash(request_id);
create index pages_ind on search_results (request_id,page);
create index users_ids on users using hash(id);
create index buttons_indd1 on buttons using hash(button_id1);
create index buttons_indd2 on buttons using hash(button_id2);

